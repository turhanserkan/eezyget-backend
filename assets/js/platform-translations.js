// Initialize language manager when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    window.languageManager = new PlatformLanguageManager();
    
    // Update current year automatically
    updateCurrentYear();
    
    // Force update content after a small delay to ensure all elements are loaded
    setTimeout(() => {
        if (window.languageManager) {
            window.languageManager.updateContent();
        }
    }, 100);
});

// Update current year in footer
function updateCurrentYear() {
    const currentYear = new Date().getFullYear();
    const yearElement = document.getElementById('current-year');
    if (yearElement) {
        yearElement.textContent = currentYear;
    }
    
    // Update copyright text with current year
    Object.keys(platformTranslations).forEach(lang => {
        if (platformTranslations[lang].footer && platformTranslations[lang].footer.copyright) {
            platformTranslations[lang].footer.copyright = platformTranslations[lang].footer.copyright.replace('{year}', currentYear);
        }
    });
}