    updateFormatComparisonCards() {
        const formatCards = document.querySelectorAll('.format-card');
        const formatTexts = this.getFormatComparisonTexts();
        
        formatCards.forEach((card, index) => {
            if (index === 0) { // Audio card
                this.updateFormatCard(card, formatTexts.audio);
            } else if (index === 1) { // Video card  
                this.updateFormatCard(card, formatTexts.video);
            }
        });
    }

    updateFormatCard(card, texts) {
        // Update header text
        const headerH3 = card.querySelector('.format-header h3');
        if (headerH3) headerH3.textContent = texts.title;
        
        // Update list items
        const listItems = card.querySelectorAll('.format-content li');
        texts.features.forEach((feature, index) => {
            if (listItems[index]) {
                // Keep the icon, update the text
                const icon = listItems[index].querySelector('i');
                listItems[index].innerHTML = '';
                if (icon) listItems[index].appendChild(icon);
                listItems[index].appendChild(document.createTextNode(feature));
            }
        });
        
        // Update use case
        const useCase = card.querySelector('.use-case');
        if (useCase) useCase.textContent = texts.useCase;
    }

